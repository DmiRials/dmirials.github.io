// Tab Switching
document.querySelectorAll('.tab-btn').forEach(btn => {
    btn.addEventListener('click', function() {
        const tabName = this.dataset.tab;
        
        // Hide all tabs
        document.querySelectorAll('.tab-content').forEach(tab => {
            tab.classList.remove('active');
        });
        
        // Remove active class from all buttons
        document.querySelectorAll('.tab-btn').forEach(b => {
            b.classList.remove('active');
        });
        
        // Show selected tab
        document.getElementById(tabName).classList.add('active');
        this.classList.add('active');
    });
});

/* ===== NAT Rules ===== */

document.getElementById('natForm').addEventListener('submit', function(e) {
    e.preventDefault();
    
    const localNetwork = document.getElementById('localNetwork').value.trim();
    const routerIp = document.getElementById('routerIp').value.trim();
    
    if (!validateIpNetwork(localNetwork)) {
        alert('Invalid local network format. Use CIDR notation (e.g., 192.168.1.0/24)');
        return;
    }
    
    if (!validateIp(routerIp)) {
        alert('Invalid router IP address.');
        return;
    }
    
    const commands = generateNatCommands(localNetwork, routerIp);
    displayOutput('nat', commands);
    maybeDownloadScript('nat', commands);
});

function generateNatCommands(localNetwork, routerIp) {
    let commands = '';
    
    commands += '# MikroTik NAT Configuration\n';
    commands += '# Generated by DmiRials NAT Generator\n';
    commands += '# Execute these commands in MikroTik WebFig or Telnet\n\n';
    
    commands += '/ip firewall nat\n';
    commands += 'add chain=srcnat src-address=' + localNetwork + ' action=masquerade comment="Masquerade local network"\n';
    
    commands += '\n# Verify with:\n';
    commands += '/ip firewall nat print\n';
    
    return commands;
}

