document.getElementById('portforwardForm').addEventListener('submit', function(e) {
    e.preventDefault();
    
    const routerIp = document.getElementById('pfRouterIp').value.trim();
    const internalIp = document.getElementById('pfInternalIp').value.trim();
    const externalPort = document.getElementById('pfExternalPort').value.trim();
    const internalPort = document.getElementById('pfInternalPort').value.trim();
    const protocol = document.getElementById('pfProtocol').value.trim();
    const hairpin = document.getElementById('hairpinToggle').checked;
    const hairpinLan = document.getElementById('hairpinLan').value.trim();
    
    if (!validateIp(routerIp)) {
        alert('Invalid router IP address.');
        return;
    }
    
    if (!validateIp(internalIp)) {
        alert('Invalid internal IP address.');
        return;
    }
    
    if (!validatePort(externalPort) || !validatePort(internalPort)) {
        alert('Invalid port number. Use 1-65535.');
        return;
    }
    
    if (hairpin && hairpinLan && !validateIpNetwork(hairpinLan)) {
        alert('Invalid LAN network for hairpin NAT. Use CIDR notation.');
        return;
    }
    
    const commands = generatePortForwardCommands(routerIp, internalIp, externalPort, internalPort, protocol, {hairpin, hairpinLan});
    displayOutput('portforward', commands);
    maybeDownloadScript('portforward', commands);
});

function generatePortForwardCommands(routerIp, internalIp, externalPort, internalPort, protocol, options = {}) {
    const {hairpin = false, hairpinLan = ''} = options;
    let commands = '';
    
    commands += '# MikroTik Port Forwarding Configuration\n';
    commands += '# Generated by DmiRials Port Forward Generator\n';
    commands += '# This forwards external port to internal device\n\n';
    
    commands += '/ip firewall nat\n';
    
    const protocols = protocol === 'tcp,udp' ? ['tcp', 'udp'] : [protocol];
    
    protocols.forEach(proto => {
        commands += 'add chain=dstnat dst-address=' + routerIp + ' protocol=' + proto + ' dst-port=' + externalPort + ' action=dnat to-addresses=' + internalIp + ' to-ports=' + internalPort + ' comment="Port forward ' + externalPort + ' -> ' + internalIp + ':' + internalPort + ' (' + proto.toUpperCase() + ')"\n';
    });

    // hairpin nat logic
    if (hairpin) {
        commands += '\n# Hairpin NAT (loopback) for LAN clients\n';
        if (hairpinLan && validateIpNetwork(hairpinLan)) {
            commands += 'add chain=srcnat src-address=' + hairpinLan + ' dst-address=' + internalIp + ' connection-nat-state=dstnat action=masquerade comment="Hairpin NAT only for dstnat connections"\n';
        } else {
            commands += 'add chain=srcnat dst-address=' + internalIp + ' action=masquerade comment="Hairpin NAT for port forward"\n';
        }
    }
    
    commands += '\n# Verify all rules:\n';
    commands += '/ip firewall nat print\n';
    
    return commands;
}

/* ===== Script download helper ===== */

function maybeDownloadScript(tabName, commands) {
    const toggle = document.getElementById(tabName + 'ScriptToggle');
    if (toggle && toggle.checked) {
        const blob = new Blob([commands], { type: 'text/plain' });
        const url = URL.createObjectURL(blob);
        const a = document.createElement('a');
        a.href = url;
        a.download = tabName + '.rsc';
        document.body.appendChild(a);
        a.click();
        document.body.removeChild(a);
        URL.revokeObjectURL(url);
    }
}

/* ===== Validation ===== */

function validateIp(ip) {
    const ipRegex = /^(\d{1,3}\.){3}\d{1,3}$/;
    if (!ipRegex.test(ip)) return false;
    
    const parts = ip.split('.');
    return parts.every(part => {
        const num = parseInt(part);
        return num >= 0 && num <= 255;
    });
}

function validateIpNetwork(network) {
    const parts = network.split('/');
    if (parts.length !== 2) return false;
    const cidr = parseInt(parts[1]);
    return validateIp(parts[0]) && cidr >= 0 && cidr <= 32;
}

function validatePort(port) {
    const num = parseInt(port);
    return num >= 1 && num <= 65535;
}

/* ===== Output Handling ===== */

function displayOutput(tabName, commands) {
    const outputId = tabName + 'Output';
    const commandId = tabName + 'CommandOutput';
    const copyId = tabName + 'CopyBtn';
    
    document.getElementById(commandId).textContent = commands;
    document.getElementById(outputId).style.display = 'block';
    
    document.getElementById(copyId).onclick = function() {
        navigator.clipboard.writeText(commands).then(() => {
            const originalText = this.textContent;
            this.textContent = '✓ Copied!';
            setTimeout(() => {
                this.textContent = originalText;
            }, 2000);
        });
    };
}

