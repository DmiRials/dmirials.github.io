document.getElementById('dnsForm').addEventListener('submit', function(e) {
    e.preventDefault();
    
    let dnsServers = [];
    
    // Get primary DNS servers (checkboxes)
    const primaryDns = document.querySelectorAll('.primary-dns:checked');
    if (primaryDns.length === 0) {
        alert('Пожалуйста, выберите хотя бы один основной DNS.');
        return;
    }
    primaryDns.forEach(checkbox => {
        dnsServers.push(checkbox.value);
    });
    
    // Get secondary DNS servers (checkboxes)
    const secondaryDns = document.querySelectorAll('.secondary-dns:checked');
    secondaryDns.forEach(checkbox => {
        dnsServers.push(checkbox.value);
    });
    
    // Add custom DNS if provided
    const customDns = document.getElementById('dnsCustom').value.trim();
    if (customDns) {
        if (!validateIp(customDns)) {
            alert('Неверный IP адрес DNS.');
            return;
        }
        dnsServers.push(customDns);
    }
    
    // Remove duplicates
    dnsServers = [...new Set(dnsServers)];
    
    // collect redirect/filter data
    const redirect = redirectToggle.checked;
    const filter = filterToggle.checked;
    const redirectRouterIp = document.getElementById('redirectRouterIp').value.trim();
    const redirectLocalSubnet = document.getElementById('redirectLocalSubnet').value.trim();
    const filterWan = document.getElementById('filterWanInterface').value.trim();
    
    if (redirect) {
        if (!validateIp(redirectRouterIp)) {
            alert('Невалидный IP роутера для редиректа.');
            return;
        }
        if (!validateIpNetwork(redirectLocalSubnet)) {
            alert('Неверный формат локальной подсети.');
            return;
        }
    }
    if (filter && !filterWan) {
        alert('Укажите интерфейс или список WAN для фильтра.');
        return;
    }
    
    const commands = generateDnsCommands(dnsServers, {redirect, redirectRouterIp, redirectLocalSubnet, filter, filterWan});
    displayOutput('dns', commands);
    maybeDownloadScript('dns', commands);
});

function generateDnsCommands(dnsServers, options = {}) {
    const {redirect, redirectRouterIp, redirectLocalSubnet, filter, filterWan} = options;
    let commands = '';
    
    commands += '# Конфигурация DNS на MikroTik\n';
    commands += '# Сгенерировано DmiRials DNS Generator\n\n';
    
    commands += '/ip dns\n';
    commands += 'set allow-remote-requests=yes\n';
    commands += 'set servers=' + dnsServers.join(',') + '\n';
    // cache-size оставляем по умолчанию, не генерируем
    const ttl = document.getElementById('cacheTtl').value;
    if (ttl === '1d') {
        commands += 'set cache-max-ttl=1d\n';
    } else {
        commands += 'set cache-max-ttl=1w\n';
    }
    
    commands += '\n# Опционально: очистить кэш DNS\n';
    commands += '/ip dns cache reset\n';
    
    commands += '\n# Проверить настройки DNS:\n';
    commands += '/ip dns print\n';
    
    // redirect all DNS to router
    if (redirect) {
        commands += '\n# Перенаправление всех DNS-запросов на роутер\n';
        commands += '/ip firewall nat\n';
        commands += 'add action=dst-nat chain=dstnat dst-port=53 protocol=udp src-address="' + redirectLocalSubnet + '" to-addresses="' + redirectRouterIp + '" to-ports=53 comment="DNS redirect to the router"\n';
        commands += 'add action=dst-nat chain=dstnat dst-port=53 protocol=tcp src-address="' + redirectLocalSubnet + '" to-addresses="' + redirectRouterIp + '" to-ports=53 comment="DNS redirect to the router"\n';
    }
    
    // block DNS from internet
    if (filter) {
        commands += '\n# Блокировка DNS запросов из Интернета\n';
        commands += '/ip firewall filter\n';
        commands += 'add action=drop chain=input comment="Blocking DNS requests from the Internet" dst-port=53 in-interface-list=' + filterWan + ' protocol=udp\n';
        commands += 'add action=drop chain=input comment="Blocking DNS requests from the Internet" dst-port=53 in-interface-list=' + filterWan + ' protocol=tcp\n';
    }
    
    return commands;
}

